package Character.Archer;

public class Ranger extends Archer{
    public void setAttributes(){
        this.setName("Ranger");
        this.setPrize(115);
        this.setAttack(14f);
        this.setDefense(5f);
        this.setHealth(8f);
        this.setSpeed(10f);
        this.setTribe("Highlander");
    }
}